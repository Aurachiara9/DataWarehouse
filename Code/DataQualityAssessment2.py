def call_llm(prompt: str, system: str=None, max_tokens: int=1500) ->str:
    provider = LLM_PROVIDER.lower()
    try:
        if provider == 'ollama':
            import requests
            messages = []
            if system:
                messages.append({"role": "system", "content": system})
            messages.append({"role":"user", "content": prompt})
            payload = {
                "model": OLLAMA_MODEL,
                "messages": messages,
                "stream": False
            }
            r = requests.post(f'{OLLAMA_BASE_URL}/api/chat', json=payload, timeout=500)
            r.raise_for_status()
            return r.json()['message']['content']
        
        else:
            raise ValueError(
                f'Unknown provider:{provider}. Choose: ollama'
            )
    except Exception as e:
        return f"[LLM Error —{provider}]{type(e).__name__}: {e}"


