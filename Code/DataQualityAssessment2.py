#Natural Language DQ Audit Report

def generate_and_save_audit(scorecard: dict, output_dir: str):
    #Prende una scorecard già esistente, invoca l'LLM per l'audit e salva il report risultante in un file Markdown.
    table_name = scorecard['table']
    SYSTEM_AUDIT = """As an expert Data Quality Analyst with a focus on Data Warehousing, you will analyze a
    structured DQ scorecard to generate clear and strategic audit reports for business stakeholders. 
    Your focus should be on identifying the issues and providing actionable recommendations based on their 
    potential business impact."""
    scorecard_json = json.dumps(scorecard, indent=2)
    PROMPT_AUDIT =f"""Below is the Data Quality Assessment scorecard for the table in a retail Data Warehouse.
                      Analyze it and produce a structured audit report with:
                      1. Executive Summary (2–3 sentences)
                      2. Issues
                      3. Recommended Cleaning Tasks (ordered list)
                      SCORECARD:
                      {scorecard_json}
                    """
    
    print(f'🤖 Calling LLM for DQ Audit Report: {table_name}...\n')
    audit_report = call_llm(PROMPT_AUDIT, system=SYSTEM_AUDIT, max_tokens=1000)
    print('═'*70)
    print(f' LLM DATA QUALITY AUDIT REPORT — {table_name}')
    print('═'*70)
    print(audit_report)
    print('═'*70)
    file_friendly_name = table_name.lower().replace(" ", "_")
    audit_output_path = os.path.join(output_dir, f'dq_audit_{file_friendly_name}.md')
    os.makedirs(output_dir, exist_ok=True)
    with open(audit_output_path, 'w', encoding='utf-8') as f:
        f.write(f'# 📊 LLM Data Quality Audit Report — {table_name}\n\n')
        f.write(f'## Generated by LLM ({LLM_PROVIDER.capitalize()})\n\n')
        f.write(audit_report)
        print(f'✅ Audit report saved → {audit_output_path}')
    with open(audit_output_path, 'r', encoding='utf-8') as f:
        content = f.read()
    display(Markdown(content))


