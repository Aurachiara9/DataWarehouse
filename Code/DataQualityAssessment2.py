# ── Rebuild a simplified scorecard dictionary for LLM analysis

def create_scorecard(table_name: str, df: pd.DataFrame, dq_df: pd.DataFrame) -> dict:
    scorecard = {
        'table': table_name,
        'rows': len(df),
        'columns': list(df.columns),
        'overall_score': round(dq_df['Score'].mean(), 4),
        'dimensions': {
            row['Dimension']: {
                'score': row['Score'],
                'issues': int(row['Issues']),
                'details': row['Details']
            }
            for _, row in dq_df.iterrows()
        }
    }

    print(json.dumps(scorecard, indent=2))
    print(f"\n📦 Table: {scorecard['table']}")
    print(f"Overall Score: {scorecard['overall_score']:.2%}\n")
    print(f'✅ {table_name} dataset and simplified scorecard ready for LLM analysis')

    return scorecard

